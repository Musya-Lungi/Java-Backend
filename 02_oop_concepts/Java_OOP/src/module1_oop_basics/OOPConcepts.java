package module1_oop_basics;

/**
 * This class explains the 4 pillars of OOP in Java.
 * - Encapsulation
 * - Inheritance
 * - Polymorphism
 * - Abstraction
 */
public class OOPConcepts {

    public static void main(String[] args) {
        System.out.println("=== 4 Main Pillars of OOP ===");
        System.out.println("1. Encapsulation: Wrapping data and methods into a single unit.");
        System.out.println("2. Inheritance: Acquiring properties of one class into another.");
        System.out.println("3. Polymorphism: Performing a task in different ways (e.g., overloading/overriding).");
        System.out.println("4. Abstraction: Hiding internal details and showing only functionality.");
    }
}
